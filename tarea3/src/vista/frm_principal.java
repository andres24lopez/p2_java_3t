    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed

obj_empleado = new empleado(
        Integer.parseInt(lbl_id.getText()), // ID del empleado a actualizar
        lbl_codigo.getText(),
        lbl_nombres.getText(),
        lbl_apellidos.getText(),
        lbl_direccion.getText(),
        lbl_telefono.getText(),
        lbl_fecha.getText(),
        cb_puesto.getSelectedIndex() // ID del puesto seleccionado en el JComboBox
    );
    
    // Llamar al método para actualizar el empleado
    obj_empleado.actualizar();

    // Actualizar la tabla de empleados
    tbl_empleado.setModel(obj_empleado.leer());
}



