     public void selec_datos (){
    int fila = this.tbl_empleado.getSelectedRow();
        lbl_id.setText(this.tbl_empleado.getValueAt(fila, 0).toString());
        this.lbl_codigo.setText(this.tbl_empleado.getValueAt(fila, 1).toString());
        this.lbl_nombres.setText(this.tbl_empleado.getValueAt(fila, 2).toString());
        this.lbl_apellidos.setText(this.tbl_empleado.getValueAt(fila, 3).toString());
        this.lbl_direccion.setText(this.tbl_empleado.getValueAt(fila, 4).toString());
        this.lbl_telefono.setText(this.tbl_empleado.getValueAt(fila, 5).toString());
        this.lbl_fecha.setText(this.tbl_empleado.getValueAt(fila, 6).toString());
    
        String puesto = this.tbl_empleado.getValueAt(fila, 7).toString();
         this.cb_puesto.setSelectedItem(puesto);
    
    }
    
